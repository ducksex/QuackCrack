def print_results(results):
    if not results:
        print("[!] No possible decoding found.")
        return
    print(f"\nPossible results ({len(results)} found):\n" + "-"*40)
    for method, result in results:
        print(f"==> Method: {method}")
        print(f"{result}\n{'-'*40}")

